(function() {
  var user = null;
  try { user = JSON.parse(localStorage.getItem('zoodom_current_user') || 'null'); } catch(e) {}

  var area    = document.getElementById('header-user-area');
  var loginLi = document.getElementById('nav-login-li');

  if (!user || !area) return;

  if (loginLi) loginLi.style.display = 'none';

  var initials  = user.name.trim().split(/\s+/).slice(0, 2).map(function(w){ return w[0].toUpperCase(); }).join('');
  var firstName = user.name.split(' ')[0];

  area.innerHTML =
    '<div class="header-user" id="__huw">' +
      '<button class="user-btn" id="__ubt" type="button">' +
        '<div class="user-avatar">' + initials + '</div>' +
        '<span class="user-name-short">' + firstName + '</span>' +
        '<span class="user-arrow">▾</span>' +
      '</button>' +
      '<div class="user-dropdown" id="__udd">' +
        '<div class="user-dropdown-header">' +
          '<div class="dd-name">' + user.name + '</div>' +
          '<div class="dd-email">' + user.email + '</div>' +
        '</div>' +
        '<ul>' +
          '<li><a href="profile.html">👤 Мій профіль</a></li>' +
          '<li class="dd-separator"></li>' +
          '<li><button class="dd-logout" onclick="authLogout()">🚪 Вийти</button></li>' +
        '</ul>' +
      '</div>' +
    '</div>';

  var btn = document.getElementById('__ubt');
  var dd  = document.getElementById('__udd');

  btn.addEventListener('click', function(e) {
    e.stopPropagation();
    dd.classList.toggle('open');
    btn.classList.toggle('open');
  });

  document.addEventListener('click', function() {
    dd.classList.remove('open');
    btn.classList.remove('open');
  });
})();

function authLogout() {
  localStorage.removeItem('zoodom_current_user');
  window.location.href = 'index.html';
}

