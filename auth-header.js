function renderCards(cat) {
  var grid = document.getElementById('blog-grid');
  grid.innerHTML = '';
  articles.forEach(function(a) {
    if (cat !== 'all' && a.cat !== cat) return;
    var card = document.createElement('div');
    card.className = 'card';
    card.dataset.cat = a.cat;
    card.innerHTML =
      '<img src="' + a.img + '" alt="' + a.title + '" style="width:100%;height:180px;object-fit:cover;"/>' +
      '<div class="card-body">' +
        a.badge +
        '<h3 style="margin-top:8px;">' + a.title + '</h3>' +
        '<p>' + a.preview + '</p>' +
        '<small style="color:#aaa;display:block;margin-top:8px;">' + a.meta + '</small>' +
        '<span class="card-read-hint">Читати статтю →</span>' +
      '</div>';
    card.addEventListener('click', function() { openModal(a); });
    grid.appendChild(card);
  });
}

// ===== ФІЛЬТР =====
function filterBlog(cat) {
  renderCards(cat);
}

// ===== МОДАЛЬНЕ ВІКНО =====
function openModal(a) {
  document.getElementById('modal-img').src = a.img;
  document.getElementById('modal-img').alt = a.title;
  document.getElementById('modal-badge').innerHTML = a.badge;
  document.getElementById('modal-title').textContent = a.title;
  document.getElementById('modal-meta').textContent = a.meta;
  document.getElementById('modal-content').innerHTML = a.full;
  document.getElementById('modal').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeModal(e) {
  if (e.target === document.getElementById('modal')) {
    document.getElementById('modal').classList.remove('open');
    document.body.style.overflow = '';
  }
}

document.addEventListener('keydown', function(e) {
  if (e.key === 'Escape') {
    document.getElementById('modal').classList.remove('open');
    document.body.style.overflow = '';
  }
});

// Кнопка закрити
document.querySelector('.modal-close').addEventListener('click', function() {
  document.getElementById('modal').classList.remove('open');
  document.body.style.overflow = '';
});

